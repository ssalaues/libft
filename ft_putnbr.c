#include "libft.h"

void	ft_putnbr(int n)
{
	char *min;
	long	nb;

	nb = n;
	min = "-2147483648";
	if (nb == -2147483648)
		while (*min)
			write(1, min++, 1);
	if (nb < 0 && nb != -2147483648)
	{
		nb = -nb;
		write(1, "-", 1);
	}
	if (nb > 9 && nb != -2147483648)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else if (nb != -2147483648)
		ft_putchar(n + '0');
}
