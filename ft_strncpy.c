#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	char *tmp;

	tmp = s1;
	while (n > 0 && *s2)
	{
		*tmp++ = *s2++;
		--n;
	}
	while (n > 0)
	{
		*tmp++ = '\0';
		--n;
	}
	return (s1);
}
