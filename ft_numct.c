#include "libft.h"

int	ft_numct(const char *s)
{
	int	ct;

	ct = 0;
	while (*s)
	{
		if (ft_isdigit(*s) && !ft_isdigit(*(s + 1)))
			ct++;
		s++;
	}
	return (ct);
}
