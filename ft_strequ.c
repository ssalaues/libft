#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	if (!s1 || !s2 || ft_strcmp(s1, s2) < 0 || ft_strcmp(s1, s2) > 0)
		return (0);
	else
		return (1);
}
